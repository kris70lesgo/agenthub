export const nodeRegistry = Object.fromEntries(
  definitions.map((definition) => [definition.kind, definition]),
) as Record<WorkflowNodeKind, WorkflowNodeDefinition>;

export const workflowNodeDefinitions = definitions;

export function createWorkflowNode(
  kind: WorkflowNodeKind,
  id: string,
  position: { x: number; y: number },
): AgentHubWorkflowNode {
  const definition = nodeRegistry[kind];
  return {
    id,
    type: "workflowNode",
    position,
    data: {
      label: definition.title,
      description: definition.description,
      kind,
      category: definition.category,
      status: "idle",
      version: definition.version,
      publisher: definition.publisher,
      color: definition.color,
      estimatedRuntime: definition.estimatedRuntime,
      estimatedCost: definition.estimatedCost,
      reputation: definition.reputation,
      inputs: definition.inputs,
      outputs: definition.outputs,
      capabilities: definition.capabilities,
      dependencies: definition.dependencies,
      environmentVariables: definition.environmentVariables,
      execution: definition.execution,
      configuration: definition.configuration ?? {},
      expanded: false,
      disabled: false,
    },
  };
}
